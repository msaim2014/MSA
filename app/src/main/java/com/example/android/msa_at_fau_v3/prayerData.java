package com.example.android.msa_at_fau_v3;

public class prayerData {
    String full_location;
    String date_for;
    String fajr;
    String dhur;
    String asr;
    String maghrib;
    String isha;
    String qibla_direction;
    String status_description;

    public String getFull_location() {
        return full_location;
    }

    public void setFull_location(String full_location) {
        this.full_location = full_location;
    }

    public String getDate_for() {
        return date_for;
    }

    public void setDate_for(String date_for) {
        this.date_for = date_for;
    }

    public String getFajr() {
        return fajr;
    }

    public void setFajr(String fajr) {
        this.fajr = fajr;
    }

    public String getDhur() {
        return dhur;
    }

    public void setDhur(String dhur) {
        this.dhur = dhur;
    }

    public String getAsr() {
        return asr;
    }

    public void setAsr(String asr) {
        this.asr = asr;
    }

    public String getMaghrib() {
        return maghrib;
    }

    public void setMaghrib(String maghrib) {
        this.maghrib = maghrib;
    }

    public String getIsha() {
        return isha;
    }

    public void setIsha(String isha) {
        this.isha = isha;
    }

    public String getQibla_direction() {
        return qibla_direction;
    }

    public void setQibla_direction(String qibla_direction) {
        this.qibla_direction = qibla_direction;
    }

    public String getStatus_description() {
        return status_description;
    }

    public void setStatus_description(String status_description) {
        this.status_description = status_description;
    }

}